package com.practice.gv._03_variable

class Example {

	static void main(String[] args) {
		// Defining a variable in lowercase
		int x = 5;

		// Defining a variable in uppercase
		int X = 6;

		// Defining a variable with the underscore in it's name
		def _Name = "Joe";

		println(x);
		println(X);
		println(_Name);
	}
}
